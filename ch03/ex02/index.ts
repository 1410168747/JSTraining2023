const MAX_PLUS_TWO = MAX + 2;

console.log(`max+1: ${MAX_PLUS_ONE}`);
console.log(`max+2: ${MAX_PLUS_TWO}`);

console.log(`max+1 === max+2: ${MAX_PLUS_ONE === MAX_PLUS_TWO}`);
